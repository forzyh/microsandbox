//! ## 计划功能
//!
//! 本模块计划提供在 microsandbox portal 中进行文件系统操作的功能，包括：
//!
//! ### 文件操作
//! - 读取文件内容
//! - 写入文件内容
//! - 追加内容到文件
//! - 删除文件
//!
//! ### 目录操作
//! - 列出目录内容
//! - 创建目录
//! - 删除目录
//! - 遍历目录树
//!
//! ### 元数据操作
//! - 获取文件/目录信息（大小、修改时间等）
//! - 检查文件/目录是否存在
//! - 获取权限信息
//!
//! ## 安全考虑
//!
//! 由于文件系统操作具有潜在的安全风险，本模块将实现以下安全措施：
//!
//! 1. **沙箱目录**: 所有操作限制在指定的沙箱目录内
//! 2. **路径验证**: 防止目录遍历攻击（如 `../`）
//! 3. **权限控制**: 限制可执行的操作类型
//! 4. **大小限制**: 防止读取/写入过大的文件
