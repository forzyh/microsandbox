// 以下是本模块包含的所有子模块，使用 pub 修饰符使其对外公开
// 其他 crate 可以通过 microsandbox_server::模块名 来访问这些模块

/// 配置模块 - 服务器配置管理
///
/// 包含服务器运行所需的所有配置项：
/// - JWT 密钥（用于令牌签名和验证）
/// - 监听地址（IP 和端口）
/// - 项目目录（存储沙箱配置和状态）
/// - 开发模式开关
pub mod config;

/// 错误处理模块 - 统一的错误类型定义
///
/// 定义了服务器可能遇到的所有错误类型：
/// - [`MicrosandboxServerError`] - 服务器级别的错误（启动失败、配置错误等）
/// - [`ServerError`] - 应用级别的错误（认证失败、资源不存在等）
/// - [`ErrorCode`] - 结构化的错误码，便于前端处理
pub mod error;

/// 请求处理器模块 - HTTP 请求的具体处理逻辑
///
/// 实现了所有 API 端点的处理函数：
/// - REST API 处理器（健康检查）
/// - JSON-RPC 处理器（沙箱管理方法）
/// - MCP 处理器（Model Context Protocol）
/// - 代理处理器（请求转发）
pub mod handler;

/// 服务器管理模块 - 服务器的生命周期管理
///
/// 提供服务器的启动、停止、密钥生成等功能：
/// - [`start()`] - 启动服务器
/// - [`stop()`] - 停止服务器
/// - [`keygen()`] - 生成 API 密钥
/// - 信号处理（SIGTERM/SIGINT）
pub mod management;

/// MCP 协议模块 - Model Context Protocol 实现
///
/// 实现了 MCP 规范定义的所有方法：
/// - `initialize` - 初始化握手
/// - `tools/list` - 列出可用工具
/// - `tools/call` - 调用工具
/// - `prompts/list` - 列出提示模板
/// - `prompts/get` - 获取提示模板
pub mod mcp;

/// 中间件模块 - 请求处理的中间层组件
///
/// 实现了多种中间件：
/// - [`logging_middleware`] - 记录请求和响应日志
/// - [`auth_middleware`] - JWT 令牌认证
/// - [`mcp_smart_auth_middleware`] - MCP 智能认证
/// - [`proxy_middleware`] - 代理请求处理
pub mod middleware;

/// 数据结构模块 - 请求和响应的数据定义
///
/// 定义了所有 API 使用的数据结构：
/// - JSON-RPC 消息结构（请求、响应、错误）
/// - 沙箱操作参数（启动、停止、获取指标）
/// - 响应结构（状态、指标、消息）
pub mod payload;

/// 端口管理模块 - 沙箱端口分配和管理
///
/// 实现了端口管理器：
/// - 自动分配可用端口（从 OS 获取）
/// - 端口持久化（文件存储）
/// - 端口唯一性检查
/// - 端口释放和回收
pub mod port;

/// 路由模块 - HTTP 路由配置
///
/// 定义了所有 API 端点的路由：
/// - `/api/v1/health` - 健康检查
/// - `/api/v1/rpc` - JSON-RPC 接口
/// - `/mcp` - MCP 协议接口
pub mod route;

/// 状态模块 - 应用状态管理
///
/// 定义了线程安全的应用状态容器：
/// - [`AppState`] - 包含配置和端口管理器
/// - 使用 `Arc` 实现多线程共享
/// - 使用 `RwLock` 实现读写同步
pub mod state;

