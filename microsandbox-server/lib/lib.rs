//! # Microsandbox Server - 微沙箱服务器模块
//!
//! 这是微沙箱项目的核心服务器模块，负责提供 HTTP API 服务来管理和控制沙箱环境。
//!
//! ## 模块概述
//!
//! 本模块实现了以下功能：
//! - **HTTP 服务器**：基于 Axum 框架构建 RESTful API 和 JSON-RPC 接口
//! - **沙箱管理**：启动、停止、监控沙箱的生命周期
//! - **认证授权**：基于 JWT 的 API 密钥认证机制
//! - **MCP 协议**：实现 Model Context Protocol，供 AI 助手调用沙箱功能
//! - **端口管理**：自动分配和管理沙箱的网络端口
//!
//! ## 架构设计
//!
//! ```text
//! ┌─────────────────────────────────────────────────────────────┐
//! │                      HTTP 请求                               │
//! └─────────────────────────────────────────────────────────────┘
//!                              │
//!                              ▼
//! ┌─────────────────────────────────────────────────────────────┐
//! │                    Middleware (中间件)                       │
//! │  • 日志记录 (logging_middleware)                              │
//! │  • 认证验证 (auth_middleware)                                 │
//! │  • MCP 智能认证 (mcp_smart_auth_middleware)                   │
//! └─────────────────────────────────────────────────────────────┘
//!                              │
//!                              ▼
//! ┌─────────────────────────────────────────────────────────────┐
//! │                      Router (路由)                           │
//! │  • /api/v1/health - 健康检查                                  │
//! │  • /api/v1/rpc - JSON-RPC 接口                               │
//! │  • /mcp - MCP 协议接口                                        │
//! └─────────────────────────────────────────────────────────────┘
//!                              │
//!                              ▼
//! ┌─────────────────────────────────────────────────────────────┐
//! │                     Handler (处理器)                         │
//! │  • health() - 健康检查处理                                    │
//! │  • json_rpc_handler() - JSON-RPC 请求分发                     │
//! │  • mcp_handler() - MCP 请求处理                               │
//! └─────────────────────────────────────────────────────────────┘
//!                              │
//!                              ▼
//! ┌─────────────────────────────────────────────────────────────┐
//! │                  AppState (应用状态)                         │
//! │  • Config - 服务器配置                                        │
//! │  • PortManager - 端口管理器                                   │
//! └─────────────────────────────────────────────────────────────┘
//!                              │
//!                              ▼
//! ┌─────────────────────────────────────────────────────────────┐
//! │              microsandbox-core (核心沙箱操作)                  │
//! │  • orchestra::up() - 启动沙箱                                 │
//! │  • orchestra::down() - 停止沙箱                               │
//! │  • orchestra::status() - 获取沙箱状态                         │
//! └─────────────────────────────────────────────────────────────┘
//! ```
//!
//! ## 主要模块说明
//!
//! | 模块 | 功能 |
//! |------|------|
//! | [`config`] | 服务器配置，包括 JWT 密钥、监听地址、项目目录等 |
//! | [`error`] | 统一的错误处理机制，定义各种错误类型和错误码 |
//! | [`handler`] | HTTP 请求处理器，实现具体的业务逻辑 |
//! | [`management`] | 服务器生命周期管理，包括启动、停止、密钥生成 |
//! | [`mcp`] | Model Context Protocol 实现，支持 AI 助手调用 |
//! | [`middleware`] | 中间件组件，包括认证、日志、代理等 |
//! | [`payload`] | 请求/响应的数据结构定义 |
//! | [`port`] | 端口管理，自动分配可用端口 |
//! | [`route`] | 路由配置，定义 API 端点 |
//! | [`state`] | 应用状态管理，线程安全地共享配置和端口管理器 |
//!
//! ## JSON-RPC 方法
//!
//! 服务器支持以下 JSON-RPC 方法：
//!
//! | 方法名 | 功能 | 说明 |
//! |--------|------|------|
//! | `sandbox.start` | 启动沙箱 | 创建并启动一个新的沙箱环境 |
//! | `sandbox.stop` | 停止沙箱 | 停止并清理沙箱资源 |
//! | `sandbox.metrics.get` | 获取指标 | 查询沙箱的 CPU、内存、磁盘使用情况 |
//! | `sandbox.repl.run` | 执行代码 | 在沙箱中运行 REPL 代码（转发到 portal） |
//! | `sandbox.command.run` | 执行命令 | 在沙箱中运行 shell 命令（转发到 portal） |
//!
//! ## MCP 工具
//!
//! 通过 MCP 协议暴露以下工具：
//!
//! | 工具名 | 功能 |
//! |--------|------|
//! | `sandbox_start` | 启动沙箱 |
//! | `sandbox_stop` | 停止沙箱 |
//! | `sandbox_run_code` | 执行代码 |
//! | `sandbox_run_command` | 执行命令 |
//! | `sandbox_get_metrics` | 获取指标 |
//!
//! ## 安全特性
//!
//! - **JWT 认证**：使用 HMAC-SHA256 算法签名 JWT 令牌
//! - **API 密钥格式**：`msb_<jwt_token>`，便于识别和管理
//! - **令牌过期**：默认 24 小时过期，可自定义
//! - **开发模式**：支持 `--dev` 模式跳过认证，便于本地调试
