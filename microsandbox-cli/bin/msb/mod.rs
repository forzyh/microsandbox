//! # msb 二进制模块
//!
//! 本模块是 `msb` 可执行文件的代码组织模块。
//! 它将命令行处理逻辑封装在 `handlers` 子模块中。
//!
//! ## 文件结构
//!
//! ```text
//! bin/msb/
//! ├── main.rs   # 程序入口，参数解析和命令分发
//! ├── mod.rs    # 本文件：模块声明
//! └── handlers.rs # 命令处理器实现
//! ```

//--------------------------------------------------------------------------------------------------
// Exports - 导出
//--------------------------------------------------------------------------------------------------

/// 导出 handlers 模块，包含所有子命令的处理函数
///
/// ### 模块可见性
/// `pub mod handlers` 使得 handlers 模块对外可见
/// main.rs 可以通过 `use msb::handlers` 导入并使用其中的函数
pub mod handlers;
